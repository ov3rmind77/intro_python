# Exemplos com dados escalares.
# Na lingugem python todos os tipos de dados são tratados como objetos.
# Assim a programação orientada a objetos é praticada na íntegra.
# Definição/dimensionamento de objetos do tipo inteiro.
a = 10
b = 27
# Definição/dimensionamento de obejeto do tipo string.
mensagem = 'Neste exemplo temos as variáveis a, b do tipo inteiro com os respectivos valores:\n'
print (mensagem)
print (a, b, '\n')
# Atribuição de novo valor pra o objeto string.
mensagem ='Agora relizaremos uma operação de adição com as variáveis.\n'
c = a + b
# Nova atribuição de valor para o objeto string.
mensagem = 'o resultado da adição é:'
print (mensagem, c,'\n')
# Podemos com os tipos de dados elementares de python, com variáveis escalares
# representar vários tipos de dados numéricos.
# o tipo de dados float, e usado para representação de valores de ponto
# flutuante e resolução de 64 bits.
a = 10.74
b = 26.26
mensagem = 'Agora temos as variáveis a,b com valores de ponto flutuante(float):'
print(mensagem)
print(a, b, '\n')
c = a + b
mensagem = 'Agora realizando a operação de adição com valores do tipo float:\n'
print(mensagem,c)
